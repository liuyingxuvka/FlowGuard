def _dedupe_public_names(*groups: tuple[str, ...]) -> list[str]:
    names: list[str] = []
    seen: set[str] = set()
    for group in groups:
        for name in group:
            if name not in seen:
                seen.add(name)
                names.append(name)
    return names


